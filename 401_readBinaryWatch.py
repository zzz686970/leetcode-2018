def readBinaryWatch(num):
	import itertools
	hours = [1,2,4,8]
	minutes = [1,2,4,8,16,32]

	result = []
	i = 0
	while i <= min(num, 4):
		j = num - i
		hour = [sum(el,0)  for el in itertools.combinations(hours,i) if sum(el,0) < 12]
		minute = [sum(el,0) for el in itertools.combinations(minutes,j) if sum(el,0) < 60]
		i += 1
		tmp = [ "{}:{:02d}".format(el[0],el[1]) for el in itertools.product(hour,minute)]
		result += tmp

	return result

print(readBinaryWatch(2))

